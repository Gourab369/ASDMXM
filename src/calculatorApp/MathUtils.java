package calculatorApp;

public class MathUtils {
	float sum(float a, float b) {
		return a+b;
	}
	float substract(float a, float b) {
		return a-b;
	}
	float divide(float a, float b) {
		return a/b;
	}
	float multiply(float a, float b) {
		return a*b;
	}
	float modulus(float a, float b) {
		return a%b;
	}
}
